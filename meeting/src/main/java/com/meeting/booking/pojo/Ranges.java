package com.meeting.booking.pojo;

public class Ranges {

	String start;
	String end;

	public Ranges(String start, String end) {
		super();
		this.start = start;
		this.end = end;
	}

	public Ranges() {
		super();
	}

	public String getStart() {
		return start;
	}

	public void setStart(String start) {
		this.start = start;
	}

	public String getEnd() {
		return end;
	}

	public void setEnd(String end) {
		this.end = end;
	}

}
